#include <iostream>
#include <cmath>

int main()
{
    std::cout << sqrt(25) << std::endl;
    std::cout << remainder(10, 3) << std::endl;
    std::cout << 10 % 3 << std::endl;
    std::cout << fmin(3, 2) << std::endl;
    std::cout << fmax(3, 2) << std::endl;
    std::cout << trunc(1.2) << std::endl;
}
