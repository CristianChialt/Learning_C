#include <iostream>

using std::string;
using std::cout;
using std::cin;
using std::endl;

int  main()
{
    int i = 0;
    cout << "While loop: " << endl;
    while (i < 10)
    {
        cout << i << endl;
        i++;
    }
    
    cout << "For loop: " << endl;
    for (int i = 0; i < 10; i++)
    {
    cout << i << endl;
    }


}